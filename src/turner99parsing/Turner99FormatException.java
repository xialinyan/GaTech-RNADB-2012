package turner99parsing;

public class Turner99FormatException extends Exception {

	private static final long serialVersionUID = 1L;

	public Turner99FormatException() {
		super();
	}

	public Turner99FormatException(String message) {
		super(message);
	}

	public Turner99FormatException(Throwable cause) {
		super(cause);
	}

	public Turner99FormatException(String message, Throwable cause) {
		super(message, cause);
	}

}
